         <H4> What is a root canal?</H4>
		A root canal is a dental procedure involving the removal of the soft center of the tooth, the pulp. The pulp is made up of nerves, connective tissue,
		and blood vessels that help the tooth grow.<br><br>

		In the majority of cases, a general dentist or endodontist will perform a root canal while you’re under local anesthesia.<br><br>
		<h5>When is a root canal needed?</h5>
		A root canal is performed when the soft inner part of a tooth, known as the pulp, is injured or becomes inflamed or infected.<br>

		The crown of the tooth — the part you can see above your gums — can remain intact even if the pulp is dead. Removing injured or infected pulp is the best 
		way to preserve the structure of the tooth.<br>

		Common causes of damage to the pulp include:
       <ul>
		<li>deep decay due to an untreated cavity.</li>
		<li>multiple dental procedures on the same tooth.</li>
		<li>a chip or crack in the tooth.</li>
		<li>an injury to the tooth (you might injure a tooth if you get hit in the mouth; the pulp can still be damaged even if the injury doesn’t crack the tooth).</li>
		</ul>
		The most common symptoms of damaged pulp include pain in your tooth, and swelling and a sensation of heat in your gums. Your dentist will examine the painful tooth and take X-rays to confirm the diagnosis.
		Your dentist may refer you to an endodontist if they think you need a root canal.
