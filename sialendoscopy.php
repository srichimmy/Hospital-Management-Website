                <b> Technique :</b> Sialendoscopy usually done under General Anaesthesia. After careful identification and dilatation of natural orifice of the duct of involved gland, sialendoscope is passed into  the duct from oral cavity through dilated orifice,  ductal system irrigated with saline through irrigation channel of the scope 
				so that duct is dilated giving a clear view of the interior of the ductal system as we advance the scope towards the gland.  Findings like mucous strands, sludge, calculi and or strictures identified during initial diagnostic scopy. Treatment according to the findings can be done either in the same sitting or in a staged manner.
                  Floating calculi (less than 3mm in Parotid duct and less than 4mm in Submandibular duct) can be removed either with basket or with forceps. Larger calculi embedded in the ductal system can be removed by Combined approach (Identification with sialendoscopy, retrieval by opening the duct and closing the duct over a stent) 
                  Strictures in the duct can be diaphragmatic (involving only mucosal lining) or transmural (involving entire thickness of the duct), can be isolated, multiple or diffuse. Strictures many of the times may be just distal to calculi making it difficult to remove the calculi. Strictures can be dilated with bougies over guide 
				  wire or with the beveled edge of modular sialendoscope itself and at the end suitable stent will be placed in the duct that can be kept insitu for few weeks. 
                  Juvenile Recurrent Parotitis (Repeated inflammation of one or both parotid glands in children) can be managed effectively by Sialendoscopy. Diagnostic scopy will be done for ductal system evaluation. Any sludge and debris will be flushed out and then ductal system will be irrigated with medication to reduce the frequency of episodes. 
                  Patients with Sjogren’s syndrome and Radio Iodine induced Sialadenitis can be offered good symptomatic improvement with Sialendocopy by irrigating and flushing out the epithelial debris and sludge as and when it is required. 
                  Sialendoscopy is the minimally invasive technique to deal with salivary ductal system pathologies. Early identification of these problems and their effective management with Sialendoscopy offers significant improvement in quality of life and avoids the need of future gland excision.<br>
