               <strong>Salivary Calculi ( Sialolithiasis):</strong><br>
                    Sialolithiasis is a benign, obstructive condition involving the formation of stones within the ducts of the major salivary glands. Sialolithiasis is the most common cause of salivary gland swelling with a reported incidence of 1 in 10000 to 1 in 30000. The etiology of salivary calculus formation is not clearly understood.
					The factors believed to be contributing to the formation of salivary stone formation are
                  <ol>
				<li><p>Inadequate  water intake and inadequate sleep leading to reduced salivary output.</p></li>
                <li><p>Poor oral hygiene .</p></li>
                <li><p>Abnormal anatomy like ductal kinks /diverticula leading to stasis of saliva.</p></li>
                <li><p>Association between Gall bladder and Renal Calculi.</p></li>
                <li><p>Questionable role of excess calcium supplementation.</p></li>
				</ol>
            <strong>Salivary Duct structures:</strong><br>
                     Stricture is a luminal narrowing of the salivary duct . Usually associated with calculi,presenting similar to calculi. Strictures may be the reason for formation of calculi or the result of calculi from recurrent infection secondary to calculi. Many of the times an impacted calculus present just behind a stricture. 
                     At times stricture may be isolated pathology presenting with meal time syndrome or recurrent painful swelling of the involved gland.<br>
            <h2>Sialendoscopy</h2>
             <p> Sialendoscopy  is the minimally invasive technique to diagnose and treat Non-neoplastic salivary gland diseases secondary to ductal system pathologies like calculi and strictures at an early stage,  so that we can preserve functional gland, can avoid gland excision that is otherwise a fairly common procedure before the advent of this technique and we can prevent complications 
			  associated with gland excision and  the consequences of hyposalivation secondary to gland excision.<br></p>
