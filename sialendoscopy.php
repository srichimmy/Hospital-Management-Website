			<b>ANATOMY OF SALIVARY GLANDS:</b> 
               The three pairs of major salivary glands in humans are the parotid, submandibular and sublingual glands.  Parotid gland present just below the ear deep to ear lobule, Submandibular gland present deep to the lower jaw bone and Sublingual gland present 
			   in floor of mouth around the duct of Submandibular gland. Saliva secreted from these glands during meals is transported through major ducts that opens into oral cavity (Stenson’s duct from Parotid gland opening in the cheek , Wharton’s duct from Submandibular gland opening in floor of mouth below tongue and Bartholin’s duct from Sublingual gland that opens into Wharton’s duct itself).  
				In addition to the major salivary glands, there are hundreds of minor salivary glands  lying under the mucosal lining of entire oral cavity and opening onto the mucosal surface via individual ducts.<br> 

			<b>Importance of saliva:</b><br>
              Saliva is very important constituent in maintainence of oral and inturn general health of a subject.
			  Hyposalivation leads to dry mouth that results in a wide range of problems like difficulty in chewing, swallowing, articulation disturbance ( difficulty in speech), bad breath, gum problems, tooth decay, oral thrush and improper digestion and related consequences. <br>

